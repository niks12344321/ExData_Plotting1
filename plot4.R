dat <- "household_power_consumption.txt"
data <- read.table(dat, header=TRUE, sep=";", stringsAsFactors=FALSE, dec=".", na.strings="?")
ReqDat <- data[data$Date %in% c("1/2/2007","2/2/2007") ,]

datetime <- strptime(paste(ReqDat$Date, ReqDat$Time, sep=" "), "%d/%m/%Y %H:%M:%S") 
GAP <- as.numeric(ReqDat$Global_active_power)
GRP <- as.numeric(ReqDat$Global_reactive_power)
volt <- as.numeric(ReqDat$Voltage)
subMetering1 <- as.numeric(ReqDat$Sub_metering_1)
subMetering2 <- as.numeric(ReqDat$Sub_metering_2)
subMetering3 <- as.numeric(ReqDat$Sub_metering_3)


png("plot4.png", width=480, height=480)
par(mfrow = c(2, 2)) 

plot(datetime, GAP, type="l", xlab="", ylab="Global Active Power", cex=0.2, lwd = 0.1)

plot(datetime, volt, type="l", xlab="datetime", ylab="voltage")

plot(datetime, subMetering1, type="l", ylab="Energy Submetering", xlab="")
lines(datetime, subMetering2, type="l", col="red")
lines(datetime, subMetering3, type="l", col="blue")
legend("topright", c("Sub_metering_1", "Sub_metering_2", "Sub_metering_3"), lty=, lwd=2.5, col=c("black", "red", "blue"), bty="o")

plot(datetime, GRP, type="l", xlab="datetime", ylab="Global_reactive_power", lwd = 0.1)

dev.off()
